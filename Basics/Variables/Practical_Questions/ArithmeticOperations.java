package Basics.Variables.Practical_Questions;

public class ArithmeticOperations {
    public static void main(String[] args) {
        int a = 10, b = 20;
        float c = 15.5f, d = 4.5f;
        double e = 50.5, f = 25.25;

        System.out.println("Addition of int: " + (a + b));
        System.out.println("Subtraction of float: " + (c - d));
        System.out.println("Multiplication of double: " + (e * f));
        System.out.println("Division of int: " + (b / a));
    }
}
