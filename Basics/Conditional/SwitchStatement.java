package Conditional;

public class SwitchStatement {
    public static void main(String[] args) {
        int x = 10;
        switch (x) {
            case 5:
                System.out.println("x is 5");
                break;
            case 10:
                System.out.println("x is 10");
                break;
            default:
                System.out.println("x is neither 5 nor 10");
        }
        
    }
}
