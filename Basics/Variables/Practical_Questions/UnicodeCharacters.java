package Basics.Variables.Practical_Questions;

public class UnicodeCharacters {
    public static void main(String[] args) {
        char unicodeChar1 = '\u0041'; // Unicode for 'A'
        char unicodeChar2 = '\u03A9'; // Unicode for 'ฮฉ'
        char unicodeChar3 = '\u0939'; // Unicode for 'เคน'

        System.out.println("Unicode character 1: " + unicodeChar1);
        System.out.println("Unicode character 2: " + unicodeChar2);
        System.out.println("Unicode character 3: " + unicodeChar3);
    }
}

